namespace Richardhj\ContaoFerienpassBundle\ApplicationSystem;


use Richardhj\ContaoFerienpassBundle\Event\UserSetApplicationEvent;
use Richardhj\ContaoFerienpassBundle\Helper\Message;


class NoOp extends AbstractApplicationSystem
{

    /**
     * Returns an array of event names this subscriber wants to listen to.
     *
     * The array keys are event names and the value can be:
     *
     *  * The method name to call (priority defaults to 0)
     *  * An array composed of the method name to call and the priority
     *  * An array of arrays composed of the method names to call and respective
     *    priorities, or 0 if unset
     *
     * For instance:
     *
     *  * array('eventName' => 'methodName')
     *  * array('eventName' => array('methodName', $priority))
     *  * array('eventName' => array(array('methodName1', $priority), array('methodName2')))
     *
     * @return array The event names to listen to
     */
    public static function getSubscribedEvents()
    {
        return [
            UserSetApplicationEvent::NAME => [
                'setNewAttendance'
            ],
        ];
    }


    /**
     * @internal param UserSetApplicationEvent $event
     */
    public function setNewAttendance()
    {
        Message::addError('Zurzeit sind keine Anmeldungen möglich. Bitte versuchen Sie es später.');
    }
}